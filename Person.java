/**
 * Person class
 */
public class Person
{
    // The name of the person.
    private String name;
    // The age of the person.
    private int age;
    
    /**
     * Person's constructor, which you can set the person's name and age.
     */
    public Person(String name, int age)
    {
        this.name = name;
        this.age = age;
    }
}